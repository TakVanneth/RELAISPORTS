

// pop up 1
function showModal1() {
    var modal1 = document.getElementById('imageModal1');
    modal1.style.display = 'block';
}


function closeModal1() {
    var modal1 = document.getElementById('imageModal1');
    modal1.style.display = 'none';
}


var showDetail1 = document.getElementById('showDetail1');
showDetail1.addEventListener('click', showModal1);

var closeModal1Button = document.getElementById('closeModal1');
closeModal1Button.addEventListener('click', closeModal1);

// end pop up 1


